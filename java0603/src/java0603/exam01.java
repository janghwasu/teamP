package java0603;

public class exam01 {

	public static void main(String[] args) {
		// for문 1~10까지 출력하는 프로그램

		for (int i = 1; i < 11; i++) {
			System.out.println(i);
		}

		// while문 으로 1~10까지 출력하는 프로그램

		int num = 1;

		while (num <= 10) {
			System.out.println(num);
			num++;
		}

	}

}
